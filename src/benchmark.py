"""
Phase 3 — Robustness benchmark using noise_mixer outputs.

Evaluates all three models on clean test set and on noisy test sets
(bw, ma, em × 6 SNR levels) generated by noise_mixer.py — same 16225 beats,
same labels, same scale as training data.
"""

from pathlib import Path
import csv
import numpy as np
import torch
from sklearn.metrics import f1_score, accuracy_score

from data_pipeline import CLASS_NAMES
from models import get_model, NUM_CLASSES

PROJECT_ROOT = Path(__file__).resolve().parents[1]
RESULTS_DIR = PROJECT_ROOT / "outputs" / "results"
MODELS_DIR = PROJECT_ROOT / "outputs" / "models"

MODEL_NAMES = ["SimpleCNN", "ResNet1D", "LightweightNet"]
NOISE_TYPES = ["bw", "ma", "em"]
SNR_LEVELS = [24, 18, 12, 6, 0, -6]
CLEAN_SNR = 999
DEVICE = torch.device("cuda" if torch.cuda.is_available() else "cpu")


def load_model_checkpoint(name: str) -> torch.nn.Module:
    """Load model from outputs/models/{name}_best.pt."""
    path = MODELS_DIR / f"{name}_best.pt"
    if not path.exists():
        raise FileNotFoundError(f"Run Phase 2 first. Missing: {path}")
    model = get_model(name).to(DEVICE)
    ckpt = torch.load(path, map_location=DEVICE, weights_only=True)
    model.load_state_dict(ckpt["model_state_dict"])
    model.eval()
    return model


def load_noisy_test(noise_type: str, snr_db: int) -> np.ndarray:
    """Load noisy test set from noise_mixer output. Shape (16225, 1, 280)."""
    suffix = f"{snr_db}dB" if snr_db >= 0 else f"{snr_db}dB"
    path = RESULTS_DIR / f"noisy_test_{noise_type}_{suffix}.npy"
    if not path.exists():
        raise FileNotFoundError(f"Run noise_mixer.py first. Missing: {path}")
    return np.load(path).astype(np.float32)


def _print_class_distribution(y: np.ndarray, label: str) -> None:
    counts = np.bincount(y, minlength=NUM_CLASSES)
    total = len(y)
    parts = [f"{CLASS_NAMES[i]}={counts[i]} ({100*counts[i]/total:.1f}%)" for i in range(NUM_CLASSES)]
    print(f"  {label}: {', '.join(parts)}")


def majority_baseline_accuracy(y: np.ndarray) -> float:
    counts = np.bincount(y, minlength=NUM_CLASSES)
    return float(np.max(counts) / len(y)) if len(y) else 0.0


def evaluate_model(model: torch.nn.Module, X: np.ndarray, y: np.ndarray, batch_size: int = 256) -> dict:
    model.eval()
    all_preds = []
    X_t = torch.from_numpy(X)
    for i in range(0, len(X), batch_size):
        batch = X_t[i : i + batch_size].to(DEVICE)
        with torch.no_grad():
            preds = model(batch).argmax(dim=1).cpu().numpy()
        all_preds.append(preds)
    y_pred = np.concatenate(all_preds)
    acc = accuracy_score(y, y_pred)
    macro_f1 = f1_score(y, y_pred, average="macro", labels=np.arange(NUM_CLASSES), zero_division=0)
    per_class = f1_score(y, y_pred, average=None, labels=np.arange(NUM_CLASSES), zero_division=0)
    return {
        "accuracy": acc,
        "macro_f1": macro_f1,
        "f1_N": per_class[0], "f1_S": per_class[1], "f1_V": per_class[2],
        "f1_F": per_class[3], "f1_Q": per_class[4],
    }


def run_benchmark() -> list[dict]:
    print("Phase 3 — Robustness benchmark (noise_mixer: bw, ma, em)")
    print("=" * 60)
    if not RESULTS_DIR.exists():
        raise FileNotFoundError(f"Run Phase 1 and noise_mixer first. Missing: {RESULTS_DIR}")

    X_test = np.load(RESULTS_DIR / "X_test.npy").astype(np.float32)
    y_test = np.load(RESULTS_DIR / "y_test.npy").astype(np.int64)
    print(f"Clean test set: {X_test.shape[0]} samples (same labels for all noisy sets)")
    _print_class_distribution(y_test, "Clean test set")
    majority_clean = majority_baseline_accuracy(y_test)

    rows = []
    for model_name in MODEL_NAMES:
        print(f"\nLoading {model_name}...")
        model = load_model_checkpoint(model_name)

        # Clean baseline
        res = evaluate_model(model, X_test, y_test)
        rows.append({
            "model": model_name, "noise_type": "clean", "snr_db": CLEAN_SNR,
            "accuracy": res["accuracy"], "macro_f1": res["macro_f1"],
            "f1_N": res["f1_N"], "f1_S": res["f1_S"], "f1_V": res["f1_V"],
            "f1_F": res["f1_F"], "f1_Q": res["f1_Q"],
        })

        # Noisy: each noise type × each SNR
        for noise_type in NOISE_TYPES:
            for snr_db in SNR_LEVELS:
                X_noisy = load_noisy_test(noise_type, snr_db)
                res = evaluate_model(model, X_noisy, y_test)
                rows.append({
                    "model": model_name, "noise_type": noise_type, "snr_db": snr_db,
                    "accuracy": res["accuracy"], "macro_f1": res["macro_f1"],
                    "f1_N": res["f1_N"], "f1_S": res["f1_S"], "f1_V": res["f1_V"],
                    "f1_F": res["f1_F"], "f1_Q": res["f1_Q"],
                })

    out_path = RESULTS_DIR / "benchmark_results.csv"
    with open(out_path, "w", newline="") as f:
        w = csv.writer(f)
        w.writerow(["model", "noise_type", "snr_db", "accuracy", "macro_f1", "f1_N", "f1_S", "f1_V", "f1_F", "f1_Q"])
        for r in rows:
            w.writerow([r["model"], r["noise_type"], r["snr_db"], r["accuracy"], r["macro_f1"],
                        r["f1_N"], r["f1_S"], r["f1_V"], r["f1_F"], r["f1_Q"]])
    print(f"\nSaved {out_path}")

    # Print results: one block per noise type (clean + 6 SNR)
    snr_order = [CLEAN_SNR] + SNR_LEVELS
    print("\n" + "=" * 60)
    print("BENCHMARK RESULTS (Macro F1 / Accuracy)")
    print("=" * 60)
    for noise_type in ["clean"] + NOISE_TYPES:
        title = "CLEAN" if noise_type == "clean" else noise_type.upper()
        print(f"\n--- {title} ---")
        print(f"{'Model':<14} {'SNR':>6} {'Acc':>8} {'MacroF1':>8}  N      S      V      F      Q")
        print("-" * 60)
        for model_name in MODEL_NAMES:
            model_rows = [r for r in rows if r["model"] == model_name and r["noise_type"] == noise_type]
            for r in model_rows:
                snr = r["snr_db"]
                snr_str = "clean" if snr == CLEAN_SNR else f"{snr} dB"
                print(f"{model_name:<14} {snr_str:>6} {r['accuracy']:>8.4f} {r['macro_f1']:>8.4f}  "
                      f"{r['f1_N']:.3f} {r['f1_S']:.3f} {r['f1_V']:.3f} {r['f1_F']:.3f} {r['f1_Q']:.3f}")
        print("-" * 60)
    print("Majority baseline (accuracy if all predictions = majority class):")
    print(f"  clean: {majority_clean:.4f}")
    print("\033[92mPHASE 3 (benchmark) COMPLETE\033[0m")
    return rows


if __name__ == "__main__":
    run_benchmark()
